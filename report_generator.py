import datetime
import os

def generate_health_report(patient_name, predictions):
    report_content = f"""
============================================================
           HEALTH DIAGNOSTIC REPORT
============================================================
Patient Name: {patient_name}
Date of Report: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
------------------------------------------------------------

DIAGNOSTIC SUMMARY:
"""
    for disease, result in predictions.items():
        report_content += f"- {disease}: {result}\n"
    
    report_content += """
------------------------------------------------------------
RECOMMENDATIONS:
1. Consult with a specialized medical professional for a 
   comprehensive clinical evaluation.
2. Maintain a balanced diet and regular physical activity.
3. Monitor vital signs such as blood pressure and glucose 
   levels consistently.
------------------------------------------------------------
This report was generated by the AI-Powered Multi-Disease 
Prediction System developed by Srustisri Panda & Team.
============================================================
"""
    
    report_path = f"{patient_name.replace(' ', '_')}_Health_Report.txt"
    with open(report_path, "w") as f:
        f.write(report_content)
    
    print(f"Report generated successfully: {report_path}")
    return report_path

if __name__ == "__main__":
    # Sample run
    sample_preds = {
        "Diabetes": "Negative",
        "Heart Disease": "Positive",
        "Kidney Disease": "Negative"
    }
    generate_health_report("Sample Patient", sample_preds)
